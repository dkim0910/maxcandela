'use client';

import { CSSProperties, useEffect, useRef } from 'react';

export interface UnlockerState {
  playing: boolean;
  source: string;
  error: string | null;
}

/**
 * The brightness-unlock engine.
 *
 * Two-mode design so toggling is INSTANT:
 *  - While `prime` is true (EDR-capable display), the HDR clip plays
 *    continuously at 2×2 px in a corner. That keeps macOS's EDR headroom
 *    ramped up the whole time — the ~1–2 s backlight ramp happens once at
 *    page load, where it's invisible (macOS compensates SDR content, so the
 *    page looks unchanged).
 *  - `enabled` then just switches the same video to fullscreen with
 *    `mix-blend-mode: multiply` (page × EDR-white = the page itself boosted
 *    into HDR range). Pure style swap → brightness changes immediately.
 *
 * Only the browser window gets brighter — a web page can't brighten other
 * apps; that's the native app's job. The PQ clip claims ~1600 nits (the XDR
 * panel's peak) so the demo pushes full brightness.
 *
 * Codec pick: HEVC/PQ mp4 where supported (Safari, Chrome w/ hw HEVC), else
 * the VP9/HLG webm. Generated by scripts/generate-hdr-video.sh.
 */
export default function BrightnessUnlocker({
  prime,
  enabled,
  onStateChange,
}: {
  prime: boolean;
  enabled: boolean;
  onStateChange?: (state: UnlockerState) => void;
}) {
  const videoRef = useRef<HTMLVideoElement>(null);
  // Keep the callback out of the effect deps so parent re-renders can never
  // re-trigger playback.
  const onStateChangeRef = useRef(onStateChange);
  onStateChangeRef.current = onStateChange;

  useEffect(() => {
    const video = videoRef.current;
    if (!video) return;

    // Use the brightest PQ clip (~1600 nits, the XDR panel's peak) so the demo
    // pushes full brightness. Chrome (no HEVC) falls back to HLG (~1000 nits).
    const canHevc = video.canPlayType('video/mp4; codecs=hvc1') !== '';
    const src = canHevc ? '/hdr/white-pq-1600.mp4' : '/hdr/white-hlg.webm';
    if (!video.currentSrc.endsWith(src)) {
      video.src = src;
      video.load();
    }

    let stale = false;
    const report = (error: string | null) =>
      onStateChangeRef.current?.({
        playing: !video.paused && !video.ended,
        source: src.split('/').pop() ?? '',
        error,
      });

    // Play while priming OR boosting. When boosting the element is fullscreen
    // (visible), so the browser won't power-save it — and re-calling play() here
    // revives the clip if the browser paused it while it was a tiny priming
    // element (Chrome pauses muted video-only "background media" to save power).
    if (prime || enabled) {
      video
        .play()
        .then(() => {
          if (!stale) report(null);
        })
        .catch((e: unknown) => {
          // AbortError covers both our own pause interrupting play() and the
          // browser's background-media power-save pause. Both are benign — only
          // surface genuine load/decode failures.
          if (stale || (e instanceof DOMException && e.name === 'AbortError')) return;
          report(e instanceof Error ? e.message : 'play() failed');
        });
    } else {
      video.pause();
      report(null);
    }

    return () => {
      stale = true;
    };
  }, [prime, enabled]);

  // Fullscreen multiply overlay while boosting; 2×2 px headroom primer in the
  // corner otherwise. Same playing video either way — the switch is instant.
  const style: CSSProperties = enabled
    ? {
        position: 'fixed',
        inset: 0,
        width: '100vw',
        height: '100vh',
        objectFit: 'cover',
        mixBlendMode: 'multiply',
        pointerEvents: 'none',
        zIndex: 9999,
      }
    : {
        position: 'fixed',
        bottom: 0,
        right: 0,
        width: 2,
        height: 2,
        opacity: 0.05,
        pointerEvents: 'none',
        zIndex: 1,
        // Not display:none — a hidden video drops out of the HDR pipeline
        // and the headroom ramp would start over on the next toggle.
        display: prime ? 'block' : 'none',
      };

  return (
    <video
      ref={videoRef}
      muted
      loop
      playsInline
      preload="auto"
      aria-hidden="true"
      onError={() =>
        onStateChangeRef.current?.({
          playing: false,
          source: '',
          error: 'video failed to load — codec unsupported?',
        })
      }
      style={style}
    />
  );
}
